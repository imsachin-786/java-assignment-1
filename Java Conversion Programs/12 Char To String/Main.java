class Main {
    public static void main(String[] args) {
        char chr = 'D';
        char[] charArray = {'D','e','v','e','n','d','e','r'};

        //For Single character
        String str1 = String.valueOf(chr);
        System.out.println(str1);

        //For char array
        String str2 = String.valueOf(charArray);
        System.out.println(str2);
    }
}